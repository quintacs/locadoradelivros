    public Cliente getDados() {

        return new Cliente(txtNomeCliente.getText(), txtCPFCliente.getText(), txtLogradouroCliente.getText(), txtNumeroCliente.getText(), txtComplementoCliente.getText(), txtCEPClliente.getText(), txtTelefoneCliente.getText(), chkStatusCliente.isSelected());
    }

    public void setDados(Cliente c) {
        txtCPFCliente.setText((c == null) ? "" : c.getCpf());
        txtNomeCliente.setText((c == null) ? "" : c.getNome());
        txtLogradouroCliente.setText((c == null) ? "" : c.getLogradouro());
        txtNumeroCliente.setText((c == null) ? "" : c.getNumero());
        txtComplementoCliente.setText((c == null) ? "" : c.getComplemento());
        txtCEPClliente.setText((c == null) ? "" : c.getCep());
        txtTelefoneCliente.setText((c == null) ? "" : c.getTelefone());
        chkStatusCliente.setSelected((c == null) ? true : c.isStatus());
        chkStatusCliente.setVisible((c == null) ? false : true);
        txtCPFCliente.setEditable(c == null);
    }

    boolean validar() {
        Cliente cliente = getDados();
        if (cliente.getNome() != null && cliente.getCpf().length() == 11 && cliente.getTelefone() != null) {
            return true;
        }
        MsgsErros.getErro("Preencha os campos obrigatorios.");
        return false;

    }
    
    private void persistir(){
        try {
            ActionControle ac = new ActionControle();
        if (validar()) {
            ac.persistir((Cliente) getDados(), "cc");
            setDados(null);
        }
            
        } catch (Exception ex) {
            MsgsErros.getErro("Erro ao enviar dados \n" + ex.toString() );
        }
    }
   
  
