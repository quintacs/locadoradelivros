    private void getListaExemplar() {
        try {            
            if(!" ".equals(txtNomeLivro.getText())&&!"".equals(txtNomeLivro.getText())){
                 boxExemplar.removeAllItems();
                for (Exemplar x : obterExemplar()) {
                    boxExemplar.addItem(x.getCod());
                }
            }
        } catch (Exception ex) {
            MsgsErros.getErro("Erro ao enviar dados \n" + ex.toString());
        }
    }

    public Emprestimo getDados() {
        try {
            date = new Date();
            Calendar calendar = UtilDate.somaDiasCalendar(7);

            return new Emprestimo(obterCodigo(), txtCpfCliente.getText(), ((String) boxExemplar.getSelectedItem()), date, null, UtilDate.calendarToDate(calendar));

        } catch (Exception ex) {
            MsgsErros.getErro("Erro ao enviar dados \n" + ex.toString());
        }
        return null;

    }

    public String obterCodigo() {
        try {
            int maior = 0;
            ActionControle ac = new ActionControle();
            ArrayList<Object> listEmprestimoObject = new ActionControle().obterTodos("et");
            for (Object o : listEmprestimoObject) {
                Emprestimo e = (Emprestimo) o;
                int aux = Integer.parseInt(e.getCod());
                if (maior < aux) {
                    maior = aux;
                }
            }

            return maior + 1 + "";
        } catch (Exception ex) {
            MsgsErros.getErro("Erro ao enviar dados \n" + ex.toString());

        }
        return null;
    }

    private ArrayList<Exemplar> obterExemplar() {
        try {
            ArrayList<Object> listExemplarObject = new ActionControle().obterTodos("xt");

            listExemplar = new ArrayList<Exemplar>();

            for (Object x : listExemplarObject) {
                Exemplar exemplar = (Exemplar) x;
                if ((txtIsbnLivro.getText()).equals(exemplar.getLivroISBN())) {
                    listExemplar.add(exemplar);
                }
                for (Emprestimo e : obterEmprestimo()) {
                    if (exemplar.getCod().equals(e.getCodExemplar())) {
                        listExemplar.remove(exemplar);
                    }

                }
            }

            return listExemplar;
        } catch (Exception ex) {
            MsgsErros.getErro("Erro ao enviar dados \n" + ex.toString());
        }
        return null;
    }

    private ArrayList<Emprestimo> obterEmprestimo() {
        try {
            ArrayList<Object> listEmprestimoObject = new ActionControle().obterTodos("et");

            listEmprestimo = new ArrayList<Emprestimo>();

            for (Object o : listEmprestimoObject) {
                Emprestimo emprestimo = (Emprestimo) o;
                String [] codEmprestimo = emprestimo.getCodExemplar().split("-");                
                //if ((txtIsbnLivro.getText().equals(emprestimo.getCodExemplar().substring(0, emprestimo.getCodExemplar().length() - 3))) && (emprestimo.getDataDevolucao() == null)) {
                if ((txtIsbnLivro.getText().equals(codEmprestimo[0])) && (emprestimo.getDataDevolucao() == null)) {
                    listEmprestimo.add(emprestimo);
                }
            }
            return listEmprestimo;
        } catch (Exception ex) {
            MsgsErros.getErro("Erro ao enviar dados \n" + ex.toString());
        }
        return null;
    }

    private void limpraTela() {
        txtCpfCliente.setText("");
        txtNomeCliente.setText("");
        txtIsbnLivro.setText("");
        txtNomeLivro.setText("");
        boxExemplar.removeAllItems();
    }

    private Cliente ObterCliente() {
        try {
            ActionControle ac = new ActionControle();
            Cliente cliente = ((Cliente) ac.obter(cc.getCpf(), "co"));
            return cliente;

        } catch (Exception ex) {
            MsgsErros.getErro("Erro ao obter cliente \n" + ex.toString());

        }
        return null;
    }

    private Livro ObterLivro() {
        Livro livro=null;        
        try {                        
            if(!" ".equals(cl.getIsbn())&&!"".equals(cl.getIsbn())){
                ActionControle ac = new ActionControle();
                 livro = ((Livro) ac.obter(cl.getIsbn(), "lo"));
                return livro;
            }

        } catch (Exception ex) {
            MsgsErros.getErro("Erro ao enviar dados \n" + ex.toString());
        }
        return livro;
    }

